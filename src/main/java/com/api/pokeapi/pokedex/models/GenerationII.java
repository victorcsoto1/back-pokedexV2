package com.api.pokeapi.pokedex.models;

public class GenerationII {

	private SpriteSet crystal;
	private SpriteSet gold;
	private SpriteSet silver;
	
	public SpriteSet getCrystal() {
		return crystal;
	}
	
	public void setCrystal(SpriteSet crystal) {
		this.crystal = crystal;
	}
	
	public SpriteSet getGold() {
		return gold;
	}
	
	public void setGold(SpriteSet gold) {
		this.gold = gold;
	}
	
	public SpriteSet getSilver() {
		return silver;
	}
	
	public void setSilver(SpriteSet silver) {
		this.silver = silver;
	}
	
}
